    import sys
    import tempfile
    if len(sys.argv) > 1:
        b = Board.load(sys.argv[1])
        out = sys.argv[2] if len(sys.argv) > 2 else tempfile.mkdtemp(prefix="gbr_")
        files = write_gerber(b, out)
        for f in files:
            sz = Path(f).stat().st_size
            print(f"{sz:>8} bytes  {f}")
    else:
        # 自检: 空板写出 11 层 Gerber
        b = Board.empty(width_mm=50, height_mm=40)
        with tempfile.TemporaryDirectory() as d:
            files = write_gerber(b, d)
            print(f"empty board → {len(files)} layers")
            for f in files:
                head = Path(f).read_text(encoding="utf-8").splitlines()[:6]
                print(f"  {Path(f).name}: {head[0][:40]}...")
        print("gerber.py 自检 ✅")
